package travis

import (
	"github.com/hashicorp/terraform-plugin-sdk/helper/schema"
)

func Provider() *schema.Provider {
	return &schema.Provider{
		Schema: map[string]*schema.Schema{
			"url": {
				Type:        schema.TypeString,
				Optional:    true,
				Description: "enterprise projects running on a custom domain",
			},
			"org": {
				Type:        schema.TypeBool,
				Optional:    true,
				Description: "open source projects on travis-ci.org",
			},
			"com": {
				Type:        schema.TypeBool,
				Optional:    true,
				Description: "private projects on travis-ci.com",
			},
			"token": {
				Type:        schema.TypeString,
				Required:    true,
				Description: "an API access token generated by the Travis CI command line client: `travis token`",
			},
		},
		ResourcesMap: map[string]*schema.Resource{
			"travis_env_var": resourceEnvVar(),
		},
		ConfigureFunc: func(d *schema.ResourceData) (interface{}, error) {
			config := Config{
				URL:   d.Get("url").(string),
				Org:   d.Get("org").(bool),
				Com:   d.Get("com").(bool),
				Token: d.Get("token").(string),
			}
			return config.Client(), nil
		},
	}
}
